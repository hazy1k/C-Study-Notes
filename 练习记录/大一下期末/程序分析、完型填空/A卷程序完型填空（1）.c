#include <stdio.h>

int main()
{
    int a[10], i, j, t;
    printf("请输入10个数字：\n");

    for(i = 0; i < 10; i++)
    {   
        scanf("%d", &a[i]);
    }
    printf("\n");

    for(i = 0; i < 9; i++)
    {
        for(j = 0; j < 9 - i; j++)
        {
            if(a[j] < a[j+1])
            {
                t = a[j];
                a[j] = a[j+1];
                a[j+1] = t;
            }
        }
    }

    printf("从大到小排序后的结果：\n");
    for(i = 0; i < 10; i++)
    {
        printf("%d ", a[i]);
    }
    printf("\n");

    return 0;
}
// 分析如下；
/*
首先定义了一个整型数组 a[10] 和三个整型变量 i、j、t。
程序提示用户输入10个数字，并将这些数字存储到数组 a 中。
接下来使用嵌套的 for 循环来实现冒泡排序：
外层循环控制趟数，共需要进行 9 趟（因为每一趟都会将一个最大的数移动到正确的位置上）。
内层循环进行相邻元素的比较和交换：
从第一个元素开始，依次比较相邻的两个元素，如果前面的元素比后面的元素小，则交换它们的位置。
完成所有趟数的循环之后，数组 a 中的元素就按照从大到小的顺序排列好了。
最后输出排序后的结果。
*/ 

